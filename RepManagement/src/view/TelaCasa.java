        casa.setVagasPerm(jTPermanente.getText());
        casa.setVagasTemp(jTTemporario.getText());
        casa.setNome(jTStatus.getText());
        casa.Save();
        
        JOptionPane.showMessageDialog(null,"Salvo com Sucesso");
